	function obtener_datos(id){
			var documento = $("#documento"+id).val();
			var nombre = $("#nombre"+id).val();
			var celular = $("#celular"+id).val();
			
	
			$("#mod_documento").val(documento);
			$("#mod_nombre").val(nombre);
			$("#mod_celular").val(celular);
			
		
		}
	
		
		

